public class ModuleResult {

    public ModuleResult(String modulename){
        this.setFailedcase(0);
        this.setPassedcase(0);
        this.setModulename(modulename);

    }
    public  String modulename;

    public String getModulename() {
        return modulename;
    }

    public int getFailedcase() {
        return failedcase;
    }

    public int getPassedcase() {
        return passedcase;
    }

    public  int passedcase;

    public void setModulename(String modulename) {
        this.modulename = modulename;
    }

    public void setPassedcase(int passedcase) {
        this.passedcase = passedcase;
    }

    public void setFailedcase(int failedcase) {
        this.failedcase = failedcase;
    }

    public  int failedcase;
}
